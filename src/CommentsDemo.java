/**
 * Comments Demo Program
 * Demonstrates different types of comments in Java.
 */

public class CommentsDemo {

    public static void main(String[] args) {

        // Single-line comment
        System.out.println("This is a single-line comment example.");

        /*
         Multi-line comment
         used for explaining multiple lines.
        */
        System.out.println("This is a multi-line comment example.");

        System.out.println("Documentation comments are written above classes or methods.");
    }
}