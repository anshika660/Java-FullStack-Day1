public class DataTypesDemo {

    public static void main(String[] args) {

        byte age = 20;
        short year = 2026;
        int marks = 950;
        long mobile = 9876543210L;
        float percentage = 89.5f;
        double cgpa = 9.99;
        char section = 'A';
        boolean passed = true;
        String name = "Anshika";

        System.out.println("===== DATA TYPES DEMO =====");
        System.out.println("Byte      : " + age);
        System.out.println("Short     : " + year);
        System.out.println("Int       : " + marks);
        System.out.println("Long      : " + mobile);
        System.out.println("Float     : " + percentage);
        System.out.println("Double    : " + cgpa);
        System.out.println("Char      : " + section);
        System.out.println("Boolean   : " + passed);
        System.out.println("String    : " + name);
    }
}